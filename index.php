<p>When Hiscox built their York office on Peasholme Green, they commissioned Make Architects—the practice behind London's Gherkin—to transform a forgotten corner of York's medieval heart. The resulting building is a landmark in its own right, featuring a striking curved glass atrium that glows amber at dusk.</p>
<p>Inside, the building houses a portion of the Hiscox Art Collection. Started in 1971 when founder Robert Hiscox purchased an etching for his office in Lloyd's of London, the collection has since grown to over 1,000 pieces.</p>
<p>Visitors can encounter works by some of the most influential artists of the 20th and 21st centuries, most of which are typically held by major national galleries.</p>

